class Location {
    constructor(location_id, city_id, city_name, name, image_url_location, location_type, location_type_id, priority, availability, geom, description, description_eng) {
        this.location_id = location_id;
        this.city_id = city_id;
        this.city_name = city_name;
        this.name = name;
        this.image_url_location = image_url_location;
        this.location_type = location_type;
        this.location_type_id = location_type_id;
        this.priority = priority;
        this.availability = availability;
        this.geom = geom;
        this.description = description;
        this.description_eng = description_eng;
    }
  }